    private static LevelDefinition Level(
        int order,
        int chapter,
        string title,
        string direction,
        string location,
        string description,
        string storyIntro,
        string storySuccess,
        string objective,
        string input,
        string output,
        string hint,
        List<BlockKind> blocks,
        List<LevelTestCase> tests) => new()
    {
        Order = order,
        Chapter = chapter,
        Title = title,
        Direction = direction,
        Location = location,
        Description = description,
        StoryIntro = storyIntro,
        StorySuccess = storySuccess,
        Objective = objective,
        InputDescription = input,
        OutputDescription = output,
        Hint = hint,
        AllowedBlocks = blocks,
        Tests = tests
    };

    private static LevelTestCase T(string name, string input, string expected, bool hidden = false) => new()
    {
        Name = name,
        Input = input,
        ExpectedOutput = expected,
        Hidden = hidden
    };
